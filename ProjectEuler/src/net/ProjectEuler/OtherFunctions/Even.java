package net.ProjectEuler.OtherFunctions;

public class Even{
    public static boolean isEven(int a){
        if(a%2 == 0)
            return true;
        else
            return false;
    }
    public static boolean isEven(long a){
        if(a%2 == 0)
            return true;
        else
            return false;
    }

}
